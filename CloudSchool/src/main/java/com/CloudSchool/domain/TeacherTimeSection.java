package com.CloudSchool.domain;

public class TeacherTimeSection {
	private int start;	//区间开始
	private int end;	//区间结束
	private int status;	//区间状态
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}
